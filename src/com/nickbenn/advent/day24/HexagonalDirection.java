package com.nickbenn.advent.day24;

enum HexagonalDirection {

  E(1, 0),
  SE(0, 1),
  SW(-1, 1),
  W(-1, 0),
  NW(0, -1),
  NE(1, -1);

  private final int x;
  private final int y;

  HexagonalDirection(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public int getX() {
    return x;
  }

  public int getY() {
    return y;
  }

}
